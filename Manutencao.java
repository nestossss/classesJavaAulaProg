package sistemaVeiculos;

/**
 * A interface Manutencao define o comportamento para qualquer veículo
 * que precisa de manutenção. Ela inclui um método para realizar a manutenção.
 */
public interface Manutencao {
    /**
     * Realiza a manutenção do veículo.
     */
    void realizarManutencao();
}
